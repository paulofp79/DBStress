"""DDL generation for the GC benchmark schema.

Generates CREATE TABLE, CREATE INDEX, and DROP statements for N
order-like tables with configurable partitioning and compression.
Each module function can be used standalone via CLI or imported
into an existing stress-test framework.
"""

from __future__ import annotations

import queue
from concurrent.futures import ThreadPoolExecutor
from dataclasses import dataclass, field
from typing import Generator, List, Optional


# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

@dataclass
class SchemaConfig:
    """User-selectable options that drive DDL generation."""

    table_prefix: str = "GCB"
    table_count: int = 10               # user-defined table count
    partition_type: str = "NONE"        # NONE | HASH | RANGE | LIST
    partition_count: int = 8            # HASH only: 4, 8, 16, 32
    range_interval: str = "MONTHLY"     # RANGE only: MONTHLY | QUARTERLY
    compression: str = "NONE"           # NONE | BASIC | ADVANCED |
                                        # HCC_QUERY_LOW | HCC_QUERY_HIGH |
                                        # HCC_ARCHIVE_LOW | HCC_ARCHIVE_HIGH
    seed_rows: int = 500                # rows per table during seeding


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

_COMPRESSION_MAP = {
    "NONE":             "",
    "BASIC":            "COMPRESS BASIC",
    "ADVANCED":         "ROW STORE COMPRESS ADVANCED",
    "HCC_QUERY_LOW":    "COLUMN STORE COMPRESS FOR QUERY LOW",
    "HCC_QUERY_HIGH":   "COLUMN STORE COMPRESS FOR QUERY HIGH",
    "HCC_ARCHIVE_LOW":  "COLUMN STORE COMPRESS FOR ARCHIVE LOW",
    "HCC_ARCHIVE_HIGH": "COLUMN STORE COMPRESS FOR ARCHIVE HIGH",
}

_LIST_VALUES = ["NEW", "PROCESSING", "SHIPPED", "DELIVERED", "CANCELLED"]
_MAX_INSERT_CHUNK_ROWS = 100000
_MAX_SCHEMA_PARALLEL_WORKERS = 32


def _table_name(prefix: str, idx: int) -> str:
    """Return the table name for a given 1-based index."""
    return f"{prefix}_ORDER_{idx:02d}"


def _compression_clause(config: SchemaConfig) -> str:
    """Return the Oracle compression DDL fragment."""
    clause = _COMPRESSION_MAP.get(config.compression.upper(), "")
    return clause


def _partition_clause(config: SchemaConfig) -> str:
    """Return the Oracle partitioning DDL fragment."""
    ptype = config.partition_type.upper()

    if ptype == "NONE":
        return ""

    if ptype == "HASH":
        count = config.partition_count
        if count not in (4, 8, 16, 32):
            count = 8
        return f"PARTITION BY HASH (order_id) PARTITIONS {count}"

    if ptype == "RANGE":
        if config.range_interval.upper() == "QUARTERLY":
            interval = "NUMTOYMINTERVAL(3, 'MONTH')"
        else:
            interval = "NUMTOYMINTERVAL(1, 'MONTH')"
        return (
            f"PARTITION BY RANGE (order_date) "
            f"INTERVAL ({interval}) "
            f"(PARTITION p_initial VALUES LESS THAN "
            f"(TO_DATE('2024-01-01', 'YYYY-MM-DD')))"
        )

    if ptype == "LIST":
        parts = ", ".join(
            f"PARTITION p_{v.lower()} VALUES ('{v}')"
            for v in _LIST_VALUES
        )
        return f"PARTITION BY LIST (status) ({parts})"

    return ""


def _create_table_ddl(config: SchemaConfig, idx: int) -> str:
    """Return the CREATE TABLE statement for one benchmark table."""
    tname = _table_name(config.table_prefix, idx)
    comp = _compression_clause(config)
    part = _partition_clause(config)

    lines = [
        f"CREATE TABLE {tname} (",
        f"    order_id       NUMBER GENERATED BY DEFAULT AS IDENTITY,",
        f"    order_ref      VARCHAR2(40)  NOT NULL,",
        f"    customer_name  VARCHAR2(100),",
        f"    status         VARCHAR2(20)  DEFAULT 'NEW',",
        f"    order_date     DATE          DEFAULT SYSDATE,",
        f"    ship_date      DATE,",
        f"    amount         NUMBER(12,2)  DEFAULT 0,",
        f"    quantity       NUMBER(8)     DEFAULT 1,",
        f"    discount       NUMBER(5,2)   DEFAULT 0,",
        f"    payload        VARCHAR2(400),",
        f"    CONSTRAINT {tname}_PK PRIMARY KEY (order_id)",
        f")",
    ]
    if comp:
        lines.append(comp)
    if part:
        lines.append(part)

    return "\n".join(lines)


def _create_indexes_ddl(config: SchemaConfig, idx: int) -> list[str]:
    """Return CREATE INDEX statements for one benchmark table."""
    tname = _table_name(config.table_prefix, idx)
    return [
        f"CREATE INDEX {tname}_REF_IX ON {tname} (order_ref)",
        f"CREATE INDEX {tname}_DATE_IX ON {tname} (order_date)",
        f"CREATE INDEX {tname}_STATUS_IX ON {tname} (status, order_date)",
    ]


def _drop_table_plsql(tname: str) -> str:
    """Return a PL/SQL block that drops a table, ignoring ORA-942."""
    return (
        f"BEGIN\n"
        f"    EXECUTE IMMEDIATE 'DROP TABLE {tname} CASCADE CONSTRAINTS PURGE';\n"
        f"EXCEPTION\n"
        f"    WHEN OTHERS THEN\n"
        f"        IF SQLCODE != -942 THEN RAISE; END IF;\n"
        f"END;"
    )


def _format_int(value: int) -> str:
    """Return a readable integer string for logs and preview text."""
    return f"{int(value):,}"


def _load_seed_rows(cursor, connection, tname: str, row_count: int) -> Generator[str, None, None]:
    """Bulk-load rows into one table in manageable SQL chunks."""
    if row_count <= 0:
        return

    inserted = 0
    while inserted < row_count:
        chunk_rows = min(_MAX_INSERT_CHUNK_ROWS, row_count - inserted)
        cursor.execute(
            f"""
            INSERT /*+ APPEND */ INTO {tname} (
                order_ref,
                customer_name,
                status,
                order_date,
                ship_date,
                amount,
                quantity,
                discount,
                payload
            )
            SELECT
                'ORD-' || LPAD(:start_row + LEVEL, 12, '0'),
                'Customer ' || TO_CHAR(:start_row + LEVEL),
                CASE MOD(:start_row + LEVEL, 5)
                    WHEN 0 THEN 'NEW'
                    WHEN 1 THEN 'PROCESSING'
                    WHEN 2 THEN 'SHIPPED'
                    WHEN 3 THEN 'DELIVERED'
                    ELSE 'CANCELLED'
                END,
                DATE '2024-01-01' + MOD(:start_row + LEVEL, 365),
                CASE
                    WHEN MOD(:start_row + LEVEL, 7) IN (0, 1) THEN NULL
                    ELSE DATE '2024-01-01' + MOD(:start_row + LEVEL, 365) + MOD(:start_row + LEVEL, 10)
                END,
                ROUND(25 + MOD(:start_row + LEVEL, 50000) / 10, 2),
                1 + MOD(:start_row + LEVEL, 20),
                ROUND(MOD(:start_row + LEVEL, 1500) / 100, 2),
                RPAD('HOTBLOCK', 400, 'X')
            FROM dual
            CONNECT BY LEVEL <= :chunk_rows
            """,
            {
                "start_row": inserted,
                "chunk_rows": chunk_rows,
            },
        )
        connection.commit()
        inserted += chunk_rows
        yield f"Loaded {_format_int(inserted)}/{_format_int(row_count)} rows into {tname}"


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def get_table_names(config: SchemaConfig) -> list[str]:
    """Return the ordered list of table names that the config would create."""
    return [
        _table_name(config.table_prefix, i)
        for i in range(1, config.table_count + 1)
    ]


def preview_ddl(config: SchemaConfig) -> str:
    """Return the full DDL script as a string without executing anything.

    Useful for dry-run / review before committing to the database.
    """
    sections: list[str] = []

    # Header comment
    sections.append(
        f"-- GC Benchmark Schema\n"
        f"-- Tables:      {config.table_count}\n"
        f"-- Rows/Table:  {_format_int(config.seed_rows)}\n"
        f"-- Total Rows:  {_format_int(config.table_count * config.seed_rows)}\n"
        f"-- Partition:   {config.partition_type}"
        + (f" ({config.partition_count})" if config.partition_type.upper() == "HASH" else "")
        + (f" ({config.range_interval})" if config.partition_type.upper() == "RANGE" else "")
        + f"\n-- Compression: {config.compression}\n"
    )

    for i in range(1, config.table_count + 1):
        tname = _table_name(config.table_prefix, i)
        sections.append(f"-- Table {i}/{config.table_count}: {tname}")
        sections.append(_drop_table_plsql(tname))
        sections.append(_create_table_ddl(config, i) + ";")
        for idx_ddl in _create_indexes_ddl(config, i):
            sections.append(idx_ddl + ";")
        sections.append("")

    return "\n\n".join(sections)


def create_schema(config: SchemaConfig, connection) -> Generator[str, None, None]:
    """Execute DDL to create the benchmark schema.

    Yields progress messages suitable for streaming to the UI.
    *connection* must be an open ``oracledb.Connection``.
    """
    total = config.table_count
    cursor = connection.cursor()

    try:
        for i in range(1, total + 1):
            tname = _table_name(config.table_prefix, i)

            # Drop if exists
            try:
                cursor.execute(_drop_table_plsql(tname))
                connection.commit()
            except Exception:
                pass  # ignore drop errors

            # Create table
            try:
                ddl = _create_table_ddl(config, i)
                cursor.execute(ddl)
                connection.commit()
                yield f"[{i}/{total}] Created table {tname}"
            except Exception as exc:
                yield f"[{i}/{total}] ERROR creating {tname}: {exc}"
                continue

            if config.seed_rows > 0:
                try:
                    yield (
                        f"[{i}/{total}] Loading {_format_int(config.seed_rows)} rows into {tname}"
                    )
                    for progress in _load_seed_rows(cursor, connection, tname, config.seed_rows):
                        yield f"[{i}/{total}] {progress}"
                except Exception as exc:
                    yield f"[{i}/{total}] ERROR loading rows into {tname}: {exc}"
                    continue

            # Create indexes
            for idx_ddl in _create_indexes_ddl(config, i):
                try:
                    cursor.execute(idx_ddl)
                    connection.commit()
                except Exception as exc:
                    idx_name = idx_ddl.split(" ON ")[0].replace("CREATE INDEX ", "")
                    yield f"[{i}/{total}] ERROR creating index {idx_name}: {exc}"
        yield (
            f"Schema creation complete — {total} tables, "
            f"{_format_int(total * config.seed_rows)} total rows requested."
        )
    finally:
        cursor.close()


def create_schema_parallel(config: SchemaConfig, connection_factory) -> Generator[str, None, None]:
    """Create benchmark tables in parallel using one database session per table."""
    total = config.table_count
    progress_queue: queue.Queue[str] = queue.Queue()
    max_workers = max(1, min(total, _MAX_SCHEMA_PARALLEL_WORKERS))

    def _worker(table_idx: int) -> None:
        tname = _table_name(config.table_prefix, table_idx)
        connection = None
        cursor = None
        try:
            connection = connection_factory()
            cursor = connection.cursor()
            progress_queue.put(f"[{table_idx}/{total}] Opened dedicated session for {tname}")

            try:
                cursor.execute(_drop_table_plsql(tname))
                connection.commit()
            except Exception:
                pass

            ddl = _create_table_ddl(config, table_idx)
            cursor.execute(ddl)
            connection.commit()
            progress_queue.put(f"[{table_idx}/{total}] Created table {tname}")

            if config.seed_rows > 0:
                progress_queue.put(
                    f"[{table_idx}/{total}] Loading {_format_int(config.seed_rows)} rows into {tname}"
                )
                for progress in _load_seed_rows(cursor, connection, tname, config.seed_rows):
                    progress_queue.put(f"[{table_idx}/{total}] {progress}")

            for idx_ddl in _create_indexes_ddl(config, table_idx):
                try:
                    cursor.execute(idx_ddl)
                    connection.commit()
                except Exception as exc:
                    idx_name = idx_ddl.split(" ON ")[0].replace("CREATE INDEX ", "")
                    progress_queue.put(
                        f"[{table_idx}/{total}] ERROR creating index {idx_name}: {exc}"
                    )
            progress_queue.put(f"[{table_idx}/{total}] Completed {tname}")
        except Exception as exc:
            progress_queue.put(f"[{table_idx}/{total}] ERROR processing {tname}: {exc}")
        finally:
            if cursor is not None:
                try:
                    cursor.close()
                except Exception:
                    pass
            if connection is not None:
                try:
                    connection.close()
                except Exception:
                    pass

    yield (
        f"Using {max_workers} parallel schema worker sessions "
        f"(requested tables: {total})."
    )

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        futures = [executor.submit(_worker, idx) for idx in range(1, total + 1)]

        while True:
            try:
                yield progress_queue.get(timeout=0.2)
                continue
            except queue.Empty:
                pass

            if all(f.done() for f in futures):
                break

        while not progress_queue.empty():
            yield progress_queue.get()

        for future in futures:
            future.result()

    yield (
        f"Schema creation complete — {total} tables, "
        f"{_format_int(total * config.seed_rows)} total rows requested."
    )


def drop_schema(config: SchemaConfig, connection) -> Generator[str, None, None]:
    """Drop all benchmark tables.

    Yields progress messages suitable for streaming to the UI.
    """
    tables = get_table_names(config)
    total = len(tables)
    cursor = connection.cursor()

    try:
        for i, tname in enumerate(tables, 1):
            try:
                cursor.execute(_drop_table_plsql(tname))
                connection.commit()
                yield f"[{i}/{total}] Dropped {tname}"
            except Exception as exc:
                yield f"[{i}/{total}] ERROR dropping {tname}: {exc}"

        yield f"Schema drop complete — {total} tables processed."
    finally:
        cursor.close()


def drop_tables_by_prefix(prefix: str, connection) -> Generator[str, None, None]:
    """Drop every benchmark table in the current schema for a given prefix."""
    prefix = (prefix or "").strip().upper()
    if not prefix:
        yield "ERROR: Prefix is required."
        return

    cursor = connection.cursor()
    try:
        cursor.execute(
            """
            SELECT table_name
            FROM   user_tables
            WHERE  table_name LIKE :name_pattern ESCAPE '\\'
            AND    REGEXP_LIKE(table_name, '^[A-Z0-9_]+_ORDER_[0-9]+$')
            ORDER  BY table_name
            """,
            {"name_pattern": f"{prefix}_ORDER_%"},
        )
        tables = [row[0] for row in cursor.fetchall()]

        if not tables:
            yield f"No tables found for prefix {prefix}."
            return

        total = len(tables)
        for i, tname in enumerate(tables, 1):
            try:
                cursor.execute(_drop_table_plsql(tname))
                connection.commit()
                yield f"[{i}/{total}] Dropped {tname}"
            except Exception as exc:
                yield f"[{i}/{total}] ERROR dropping {tname}: {exc}"

        yield f"Prefix drop complete — {total} tables processed for {prefix}."
    finally:
        cursor.close()
